import math
'''функции для рассчета площади и периметра круга'''

def area(r):
    return math.pi * r * r
'''На вход поступают два значения: число пи и радиус круга. По мат формуле производится операция'''

def perimeter(r):
    return 2 * math.pi * r
'''На вход поступают два значения: число пи и радиус круга. По мат формуле производится операция'''
