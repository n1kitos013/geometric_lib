'''Формулы для подсчета периметра и площади треугольника'''
def area(a, b, c):
    return (a + b + c) / 2
'''На вход поступает 3 параметра - стороны треугольника. В соответствии с формулой производится операция'''

def perimeter(a, b, c):
    return a + b + c
